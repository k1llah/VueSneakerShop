    if (dropdowns.value[index]) {
      gsap.to(dropdownContent, { height: "350px", padding: "20px", duration: 0.7, ease: "power2.out" });
    } else {
      gsap.to(dropdownContent, { height: "0px", duration: 0.7, ease: "power2.out" });
    }
  }
};
